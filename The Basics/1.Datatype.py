# Integer
int_var = 10
print(f"int: {int_var} ({type(int_var)})")

# Float
float_var = 10.5
print(f"float: {float_var} ({type(float_var)})")

# String
str_var = "Hello, Python!"
print(f"str: {str_var} ({type(str_var)})")

# Boolean
bool_var = True
print(f"bool: {bool_var} ({type(bool_var)})")

# List
list_var = [1, 2, 3, "a", "b"]
print(f"list: {list_var} ({type(list_var)})")

# Tuple
tuple_var = (1, 2, 3)
print(f"tuple: {tuple_var} ({type(tuple_var)})")

# Set
set_var = {1, 2, 3}
print(f"set: {set_var} ({type(set_var)})")

# Dictionary
dict_var = {"name": "Alice", "age": 25}
print(f"dict: {dict_var} ({type(dict_var)})")

# NoneType
none_var = None
print(f"NoneType: {none_var} ({type(none_var)})")